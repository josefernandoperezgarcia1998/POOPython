
#Paradigma:  Conjunto de cosas que sirven como un modelo o ejemplo

#POO: Paradigma de programación es un paradigma basado en creación de objetos que pueden contener datos y funciones.
    # Temas: Abstracción, Encapsulamiento, Herencia y Polimorfismo.
    # Un objeto: Combinación de propiedades y atributos, junto con una serie de métodos que puede ejecutar.
    # Alan Kay: Acuñó por primera vez el término de POO en los 60's.
